export function getDailyQuote(): IslamicQuote {
  const dayOfYear = Math.floor(
    (Date.now() - new Date(new Date().getFullYear(), 0, 0).getTime()) / 86_400_000,
  )
  return ISLAMIC_QUOTES[dayOfYear % ISLAMIC_QUOTES.length]
}
